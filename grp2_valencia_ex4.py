# #1 Arithmetic Operations using int, float, and complex

# Integer
a = 10
b = 5

print("INT OPERATIONS")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Float
x = 10.5
y = 2.5

print("\nFLOAT OPERATIONS")
print("Addition:", x + y)
print("Subtraction:", x - y)
print("Multiplication:", x * y)
print("Division:", x / y)
print("Modulus:", x % y)
print("Exponent:", x ** y)

# Complex
c1 = 2 + 3j
c2 = 1 + 4j

print("\nCOMPLEX OPERATIONS")
print("Addition:", c1 + c2)
print("Subtraction:", c1 - c2)
print("Multiplication:", c1 * c2)
print("Division:", c1 / c2)


